-- CAR SPECS (normalized, nullable fields)
CREATE TABLE IF NOT EXISTS car_specs (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  engine_oil_viscosity_id INT REFERENCES engine_oil_viscosity(id) ON DELETE SET NULL,
  engine_oil_quantity_id INT REFERENCES engine_oil_quantity(id) ON DELETE SET NULL,
  engine_oil_filter_id INT REFERENCES engine_oil_filter(id) ON DELETE SET NULL,
  brake_fluid_type_id INT REFERENCES brake_fluid_type(id) ON DELETE SET NULL,
  brake_pad_id INT REFERENCES brake_pad(id) ON DELETE SET NULL,
  brake_rotor_id INT REFERENCES brake_rotor(id) ON DELETE SET NULL,
  tire_size_id INT REFERENCES tire_size(id) ON DELETE SET NULL,
  tire_type_id INT REFERENCES tire_type(id) ON DELETE SET NULL,
  transmission_fluid_type_id INT REFERENCES transmission_fluid_type(id) ON DELETE SET NULL,
  transmission_fluid_quantity_id INT REFERENCES transmission_fluid_quantity(id) ON DELETE SET NULL,
  coolant_type_id INT REFERENCES coolant_type(id) ON DELETE SET NULL,
  engine_air_filter_id INT REFERENCES engine_air_filter(id) ON DELETE SET NULL,
  cabin_air_filter_id INT REFERENCES cabin_air_filter(id) ON DELETE SET NULL,
  wiper_blade_size_passenger_id INT REFERENCES wiper_blade_size(id) ON DELETE SET NULL,
  wiper_blade_size_driver_id INT REFERENCES wiper_blade_size(id) ON DELETE SET NULL,
  headlight_id INT REFERENCES headlight(id) ON DELETE SET NULL,
  taillight_id INT REFERENCES taillight(id) ON DELETE SET NULL,
  brake_light_id INT REFERENCES brake_light(id) ON DELETE SET NULL,
  turn_signal_light_id INT REFERENCES turn_signal_light(id) ON DELETE SET NULL,
  license_plate_light_id INT REFERENCES license_plate_light(id) ON DELETE SET NULL,
  battery_id INT REFERENCES battery(id) ON DELETE SET NULL,
  serpentine_belt_id INT REFERENCES serpentine_belt(id) ON DELETE SET NULL,
  thermostat_id INT REFERENCES thermostat(id) ON DELETE SET NULL,
  license_plate_number TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);